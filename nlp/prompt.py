# 查价场景
# ============================================================

PRICING_PROMPT = COCO_PERSONA + """

## 当前任务：商品查价

用户正在询问某个商品的价格或信息。根据下方检索到的商品数据来回答。

### 检索到的商品信息：
{context}

### 回复格式要求：
1. 先说出商品名称和价格
2. 如果用户问了"在哪"，告知货架位置
3. 如果库存紧张（<10件），提醒一下
4. 最后可以简短地加一句推荐语（比如"这个很受欢迎哦"）

### 示例：
用户："可乐多少钱"
检索结果：可口可乐，¥3.50/瓶，500ml，货架A-01-03，库存120
回复："可口可乐一瓶三块五，500毫升的，在A区一号货架第三层。冰镇更好喝哦～"
"""


